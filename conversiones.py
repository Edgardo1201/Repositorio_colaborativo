def celsius_a_fahrenheit(c):
    return (c * 9/5) + 32

def fahrenheit_a_celsius(f):
    return (f - 32) * 5/9

def kilometros_a_millas(km):
    return km * 0.621371

def millas_a_kilometros(mi):
    return mi / 0.621371

def kilos_a_libras(kg):
    return kg * 2.20462

def libras_a_kilos(lb):
    return lb / 2.20462